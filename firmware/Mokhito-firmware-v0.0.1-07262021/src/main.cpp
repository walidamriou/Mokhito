#include <Arduino.h>
uint32_t pin = 2;
uint32_t ontime,offtime,duty;
float freq,period;
void setup() {
  Serial.begin(9600);
  pinMode(pin, INPUT);
}

void loop() {
  ontime = pulseIn(pin,HIGH);
  offtime = pulseIn(pin,LOW);
  period = ontime+offtime; 
  Serial.print(period);
  Serial.println(",");
}